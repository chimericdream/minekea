package com.chimericdream.minekea.util;

public class ScreenHelpers {
    public static final int ROW_HEIGHT = 18;

    public static int getInventorySize(int rowCount) {
        return rowCount * 9;
    }

    public static int getPlayerInventoryOffset(int rowCount) {
        return 67 + ((rowCount - 1) * ROW_HEIGHT);
    }

    public static int getPlayerHotbarOffset(int rowCount) {
        return getPlayerInventoryOffset(rowCount) + (3 * ROW_HEIGHT) + 4;
    }
}
